# 숫자를 2개 입력받아 큰수와 작은수를 출력하시오

# 예 : 5와 8중 큰수는 8, 작은수는 5 입니다

num1 = int(input('첫번째 숫자:'))
num2 = int(input('두번째 숫자:'))
max =  max(num1 ,num2)
min =  min(num1 ,num2)
print(f'{num1}와 {num2}중 큰수는 {max}, 작은수는 {min}입니다')








